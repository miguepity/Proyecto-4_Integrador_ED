    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Scanner sc = null;
        File archivo = null;
        try {
            JFileChooser file = new JFileChooser();
            file.showOpenDialog(this);
            archivo = file.getSelectedFile();
            sc = new Scanner(archivo);
            sc.useDelimiter(",");
            String nombre;
            while (sc.hasNext()) {
                nombre = sc.next();
                if (nombre.contains(" y ")) {
                    person_list.insert(nombre, 2, person_list.size);
                } else {
                    person_list.insert(nombre, 1, person_list.size);
                }
                sc.nextLine();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "NO se cargaron las personas con exito!");
        } finally {
            sc.close();
        }
        JOptionPane.showMessageDialog(rootPane, "Se cargaron las personas con exito!");
        System.out.println("imprimir lista");
        person_list.Print_Lista();
    }//GEN-LAST:event_jButton1ActionPerformed

