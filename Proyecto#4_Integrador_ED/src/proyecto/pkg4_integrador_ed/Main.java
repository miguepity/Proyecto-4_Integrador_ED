    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here: 
        int tmp_personas = 0;
        Random rnd = new Random();
        random = rnd.nextInt(Lista_Personas.getSize());
        Object father = null;
        for (int j = 0; j < numSets; j++) {
            for (int i = 0; i < numGrupos; i++) {
                while (tmp_personas < numPersonGrupo) {
                    if (tmp_personas == 0 && !numRandom.isEmpty()) {
                        father = Lista_Personas.get(random).getName();
                        graph.addVertex(father);
                    }
                    if (numRandom.isEmpty()) {
                        numRandom.add(random);
                        personas.add(Lista_Personas.get(random).getName());
                        tmp_personas += Lista_Personas.get(random).getPareja();
                        father = Lista_Personas.get(random).getName();
                        graph.addVertex(father);
                    } else if (numRandom.contains(random)) {
                        random = rnd.nextInt(Lista_Personas.getSize());
                    } else {
                        numRandom.add(random);
                        personas.add(Lista_Personas.get(random).getName());
                        tmp_personas += Lista_Personas.get(random).getPareja();

                        graph.addVertex(Lista_Personas.get(random).getName());
                        graph.addEdge("Edge" + j + i + tmp_personas, Lista_Personas.get(random).getName(), father, EdgeType.DIRECTED);
                    }
                }
                System.out.println(personas.size() + " numero de grupo " + i);
                group_list.insert(personas, i);
                tmp_personas = 0;
                personas = new ArrayList();
            }
            set_list.insert(group_list, j);
            numRandom = new ArrayList();
        }

        System.out.println("probando ingreso de personas");
        for (int l = 0; l < numSets; l++) {
            for (int j = 0; j < numGrupos; j++) {
                for (int i = 0; i < set_list.get(l).list_person.get(j).personas.size(); i++) {
                    System.out.println(set_list.get(l).list_person.get(j).personas.get(i));
                }
                System.out.println("otro grupo");
            }
            System.out.println("otro set");
        }
        JOptionPane.showMessageDialog(rootPane, "Los sets estan completos puede pasar a ver el grafo!!");
    }//GEN-LAST:event_jButton4ActionPerformed

